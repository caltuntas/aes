uint8_t mul(uint8_t coefficient, uint8_t val) 
{
  uint8_t sum =0;
  for (int i=0; i<8; i++) {
    uint8_t temp =val;
    if (((1 << i) & coefficient) > 0) {
      for (int j=0; j<i; j++) {
        uint8_t t2 = temp << 1;
        if ((t2 >> 1) != temp){
          temp = t2 ^ 0x1B;
        } else {
          temp = t2;
        }
      }
      sum = sum ^ temp;
    }
  }
  return sum;
}

void mix_cols(uint8_t m[4][4], uint8_t *state)
{
  uint8_t res[16]={0};

  uint8_t b = 0;
  int counter =0;
  for (int k=0; k<4; k++) {
    for (int row=0; row<4; row++) {
      for (int col=0; col<4; col++) {
        uint8_t ce = m[row][col];
        uint8_t i = col + (k * 4);
        uint8_t s = state[i];
        b = b ^ mul(ce,s);
      }
      res[counter] = b;
      b = 0;
      counter++;
    }
  }
  memcpy(state,res,16); 
}

void mix_columns(uint8_t *state)
{
  uint8_t m[4][4] = {
    {2,3,1,1},
    {1,2,3,1},
    {1,1,2,3},
    {3,1,1,2}
  };
  mix_cols(m,state);
}

